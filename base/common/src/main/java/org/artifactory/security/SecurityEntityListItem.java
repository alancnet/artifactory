package org.artifactory.security;

/**
 * @author Noam Y. Tenne
 */
public class SecurityEntityListItem {

    private String name;
    private String uri;

    public SecurityEntityListItem() {
    }

    public SecurityEntityListItem(String name, String uri) {
        this.name = name;
        this.uri = uri;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUri() {
        return uri;
    }

    public void setUri(String uri) {
        this.uri = uri;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (!(o instanceof SecurityEntityListItem)) {
            return false;
        }

        SecurityEntityListItem that = (SecurityEntityListItem) o;

        if (name != null ? !name.equals(that.name) : that.name != null) {
            return false;
        }
        if (uri != null ? !uri.equals(that.uri) : that.uri != null) {
            return false;
        }

        return true;
    }

    @Override
    public int hashCode() {
        int result = name != null ? name.hashCode() : 0;
        result = 31 * result + (uri != null ? uri.hashCode() : 0);
        return result;
    }
}
